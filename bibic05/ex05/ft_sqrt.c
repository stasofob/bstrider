int	ft_sqrt(int nb)
{
	int cnt;

	cnt = 1;
	while (cnt * cnt < nb)
	{
		cnt++;
	}
	if ((cnt * cnt) == nb)
		nb = cnt;
	else
		nb = 0;
	return (nb);
}
