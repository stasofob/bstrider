int	ft_is_prime(int nb)
{
	int mod;
	int cnt;

	mod = 1;
	cnt = 2;
	if (nb == 2)
		return (1);
	else
		while (cnt < nb && mod != 0)
		{
			mod = nb % cnt;
			cnt++;
		}
	return (cnt == nb) ? (1) : (0);
}
