char	*ft_strlowcase(char *str)
{
	char *temp;

	temp = str;
	while (*temp != '\0')
	{
		if (*temp >= 'A' && *temp <= 'Z')
		{
			*temp = *temp + 32;
		}
		temp++;
	}
	return (str);
}
